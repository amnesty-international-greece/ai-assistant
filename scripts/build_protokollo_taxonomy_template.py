# ── Style helpers ────────────────────────────────────────────────────────────


def _style_header(cell):
    cell.font = Font(bold=True, color="FFFFFF", size=11)
    cell.fill = PatternFill(start_color="000000", end_color="000000", fill_type="solid")
    cell.alignment = Alignment(horizontal="left", vertical="center", wrap_text=False)


def _style_body(cell, *, wrap=True):
    cell.alignment = Alignment(horizontal="left", vertical="top", wrap_text=wrap)


# ── Build the workbook ───────────────────────────────────────────────────────


def build_workbook(dest: Path) -> None:
    wb = Workbook()

    # ─ Ετικέτες ──────────────────────────────────────────────────────────
    ws = wb.active
    ws.title = "Ετικέτες"
    ws.append(["Ετικέτα", "Περιγραφή / Κανόνας χρήσης"])
    _style_header(ws["A1"])
    _style_header(ws["B1"])
    for tag, desc in TAGS:
        ws.append([tag, desc])
    # Column widths + word-wrap on B
    ws.column_dimensions["A"].width = 20
    ws.column_dimensions["B"].width = 95
    for row in ws.iter_rows(min_row=2, max_col=2):
        _style_body(row[0], wrap=False)
        _style_body(row[1], wrap=True)
    # Freeze header row
    ws.freeze_panes = "A2"

    # ─ Κατηγορίες ────────────────────────────────────────────────────────
    # Two columns only - title pattern + default tags - matching the live
    # πρωτόκολλο xlsx convention.  Κύρια Σημεία conventions live in code
    # (workflows pre-fill them) rather than in this reference sheet.
    ws2 = wb.create_sheet("Κατηγορίες")
    ws2.append(["Τίτλος", "Ετικέτες"])
    for col_letter in ("A", "B"):
        _style_header(ws2[f"{col_letter}1"])
    for pattern, default_tags in CATEGORIES:
        ws2.append([pattern, default_tags])
    ws2.column_dimensions["A"].width = 62
    ws2.column_dimensions["B"].width = 45
    for row in ws2.iter_rows(min_row=2, max_col=2):
        for cell in row:
            _style_body(cell, wrap=True)
    ws2.freeze_panes = "A2"

    dest.parent.mkdir(parents=True, exist_ok=True)
    wb.save(dest)
    print(f"[OK] Wrote {dest}")
    print(f"  • Tab 'Ετικέτες'   - {len(TAGS)} tags")
    print(f"  • Tab 'Κατηγορίες' - {len(CATEGORIES)} canonical patterns")


if __name__ == "__main__":
    project_root = Path(__file__).resolve().parent.parent
    out = project_root / "assets" / "protokollo_taxonomy_template.xlsx"
    build_workbook(out)
